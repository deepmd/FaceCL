import random

from PIL import ImageFilter
from torchvision.transforms import transforms


class TwoViewsTransform:
    """Take two views of one image as the query and key."""

    def __init__(self, base_transform):
        self.base_transform = base_transform

    def __call__(self, x):
        q = self.base_transform(x)
        k = self.base_transform(x)
        return [q, k]


class GaussianBlur(object):
    """Gaussian blur augmentation in SimCLR https://arxiv.org/abs/2002.05709"""

    def __init__(self, sigma=[.1, 2.]):
        self.sigma = sigma

    def __call__(self, x):
        sigma = random.uniform(self.sigma[0], self.sigma[1])
        x = x.filter(ImageFilter.GaussianBlur(radius=sigma))
        return x


def get_transform(aug_name):
    transform = [transforms.ToPILImage()]
    if aug_name == "MoCo2":
        transform.extend([
            transforms.RandomResizedCrop(112, scale=(0.2, 1.)),
            transforms.RandomApply([
                transforms.ColorJitter(0.4, 0.4, 0.4, 0.1)  # not strengthened
            ], p=0.8),
            transforms.RandomGrayscale(p=0.2),
            transforms.RandomApply([GaussianBlur([.1, 2.])], p=0.5),
            transforms.RandomHorizontalFlip()
        ])
    elif aug_name == "MoCo2_NoCrop":
        transform.extend([
            transforms.RandomApply([
                transforms.ColorJitter(0.4, 0.4, 0.4, 0.1)  # not strengthened
            ], p=0.8),
            transforms.RandomGrayscale(p=0.2),
            transforms.RandomApply([GaussianBlur([.1, 2.])], p=0.5),
            transforms.RandomHorizontalFlip()
        ])
    elif aug_name == "ArcFace":
        transform.extend([
            transforms.RandomHorizontalFlip()
        ])
    else:
        raise ValueError(f"Specified augmentation '{aug_name}' is not valid.")
    transform.extend([
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.5, 0.5, 0.5], std=[0.5, 0.5, 0.5])
    ])
    return TwoViewsTransform(transforms.Compose(transform))
